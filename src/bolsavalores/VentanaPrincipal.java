    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed

        NombreValor = ListaValores.getSelectedItem().toString();
        int NuevaPosicion;

        //Comprobamos que el Valor Exista en Memoria
        if ((NuevaPosicion = DatosValores.BuscarValor(NombreValor)) != -1)
        {
            //Borramos el Valor de la BBDD
            DatosValores.EliminarValor(NombreValor);
        
            //Actualizamos el Combo con los Datos de Memoria
            DatosValores.RellenaCombo(ListaValores);

            //Seleccionamos el Campo Anterior
            if (NuevaPosicion != 0)
                NuevaPosicion --;
            
            //Nos Posicionamos en el Campo Anterior
            PosicionarseEnValorNum(NuevaPosicion);
        }
        else
            System.out.println("El valor '"+NombreValor+"' No Existe en Memoria");
            
        //Establecemos la Activacion y Desactivacion de Botones
        ActivaDesactivaBotones();
    }//GEN-LAST:event_EliminarActionPerformed

