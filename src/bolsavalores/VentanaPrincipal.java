    private void ActivaDesactivaBotones()
    {
        //Desactivamos Todos por Defecto
        Eliminar.setEnabled(false);
        ActLocal.setEnabled(false);
        ActManual.setEnabled(false);
        ActURL.setEnabled(false);
        Insertar.setEnabled(false);
        
        //Si hay Marcado algun Valor en el Combo
        if (!NombreValor.equals(""))
        {
            //Si el Nombre del Valor ya Existe
            if (DatosValores.BuscarValor(NombreValor) != -1)
            {
                Eliminar.setEnabled(true);
                ActLocal.setEnabled(true);
                ActManual.setEnabled(true);
                
                //Si la URL esta Indicada
                if (!URLDatos.getText().equals(""))
                    ActURL.setEnabled(true);
            }
            //Si el Nombre del Valor No Existe
            else
                Insertar.setEnabled(true);
        }
    }
    
    private void ActLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActLocalActionPerformed

        String NombreFichero;
        JFileChooser fc = new JFileChooser();

        //Mostrar la ventana para abrir archivo
        int respuesta = fc.showOpenDialog(this);

        //Comprobar si se ha pulsado Aceptar
        if (respuesta == JFileChooser.APPROVE_OPTION)
        {
            //Crear un objeto File con el archivo elegido
            File archivoElegido = fc.getSelectedFile();

            //Obtenemos la Ruta + Nombre del Fichero
            NombreFichero = archivoElegido.getPath();
            
            //Abrimos el Fichero
            if (DatosHistoricos.IncluirHistoricosLocal(NombreFichero, NombreValor, RedondeoValor) == true)
            {
                DatosHistoricos.CargaGrill(TablaHistorica);
                RellenaTecnico();
            }
        }
    }//GEN-LAST:event_ActLocalActionPerformed

    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed

        //Comprobamos que el Valor no Exista en Memoria
        if (DatosValores.BuscarValor(NombreValor) == -1)
        {
            //Incluimos el Nuevo Valor en Memoria
            DatosValores.IncluirNuevoValor(NombreValor, URLDatos.getText(), RedondeoValor);

            //Actualizamos el Combo con los Datos de Memoria
            DatosValores.RellenaCombo(ListaValores);

            //Seleccionamos de Nuevo el Valor Incluido
            PosicionarseEnValorNom(NombreValor);
        }
        else
            System.out.println("El valor '"+NombreValor+"' ya Existe en Memoria");
        
        //Establecemos la Activacion y Desactivacion de Botones
        ActivaDesactivaBotones();
    }//GEN-LAST:event_InsertarActionPerformed

    private void PosicionarseEnValorNom(String PE_NombreValor)
    {
        //Buscamos la Posicion del Valor
        int Posicion = DatosValores.BuscarValor(PE_NombreValor);

        //Posicionamos el Combo Box
        ListaValores.setSelectedIndex(Posicion);
        URLDatos.setText(DatosValores.Matrix[Posicion].URLHistorico);
        NombreValor   = PE_NombreValor;
        RedondeoValor = DatosValores.Matrix[Posicion].Redondeo;

        //Si hemos Definido un Numero Maximo de Valores
        if (cfg.MaximoHistoricos > 0)
        {
            //Cargamos los Datos Historicos del Valor
            if (DatosHistoricos.IncluirHistoricosMemoria(cfg.RutaDatos, NombreValor, RedondeoValor))
            {
                DatosHistoricos.CargaGrill(TablaHistorica);
                RellenaTecnico();
            }
        }
    }
            
    private void PosicionarseEnValorNum(int PE_Posicion)
    {
        //Posicionamos el Combo Box
        ListaValores.setSelectedIndex(PE_Posicion);        
        URLDatos.setText(DatosValores.Matrix[PE_Posicion].URLHistorico);
        NombreValor   = DatosValores.Matrix[PE_Posicion].NombreValor;
        RedondeoValor = DatosValores.Matrix[PE_Posicion].Redondeo;
        
        //Si hemos Definido un Numero Maximo de Valores
        if (cfg.MaximoHistoricos > 0)
        {
            //Cargamos los Datos Historicos del Valor
            if (DatosHistoricos.IncluirHistoricosMemoria(cfg.RutaDatos, NombreValor, RedondeoValor))
            {
                DatosHistoricos.CargaGrill(TablaHistorica);
                RellenaTecnico();
            }                
        }
    }
    
    private void RellenaTecnico()
    {
        TecnicoFecha.setText(DatosHistoricos.Matrix[0].Fecha);
        TecnicoR2.setText(String.valueOf(DatosHistoricos.Matrix[0].R2));
        TecnicoR1.setText(String.valueOf(DatosHistoricos.Matrix[0].R1));
        TecnicoPP.setText(String.valueOf(DatosHistoricos.Matrix[0].PivotPoint));
        TecnicoS1.setText(String.valueOf(DatosHistoricos.Matrix[0].S1));
        TecnicoS2.setText(String.valueOf(DatosHistoricos.Matrix[0].S2));
    }
    
    private void ListaValoresItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ListaValoresItemStateChanged
        
        //Una vez Desmarcado el Valor Seleccionado
        if (evt.getStateChange() == ItemEvent.DESELECTED)
        {   
            System.out.println("ListaValoresItemStateChanged: "+NombreValor+" - "+ListaValores.getSelectedItem().toString());
            NombreValor = ListaValores.getSelectedItem().toString();            
            
            //Reiniciar Grill y Texto de URL
            URLDatos.setText("");
            reiniciarJTable(TablaHistorica);
            
            //Si hay un Valor Seleccionado
            if (!NombreValor.equals(""))
            {
                int Posicion;

                //Comprobamos que el Valor Exista en la BBDD
                if ((Posicion = DatosValores.BuscarValor(NombreValor)) != -1)
                {
                    //Nos Posicionamos en el Valor Seleccionado
                    PosicionarseEnValorNum(Posicion);
                }
            }
            //Establecemos la Activacion y Desactivacion de Botones
            ActivaDesactivaBotones();
        }
    }//GEN-LAST:event_ListaValoresItemStateChanged

