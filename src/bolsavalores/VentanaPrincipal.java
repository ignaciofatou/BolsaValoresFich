    public static void reiniciarJTable(javax.swing.JTable Tabla)
    {
        System.out.println("reiniciarJTable");
        String Cabecera[]={"Nombre", "Fecha", "Cierre", "Máximo", "Mínimo", "Variación", "%Var", "S2", "S1", "PivotP", "R1", "R2"};
        DefaultTableModel modelo = new DefaultTableModel(null, Cabecera);
        Tabla.setModel(modelo);
        int Anchos[]={120, 70, 50, 50, 50, 50, 50, 50, 50, 50, 50, 50};
        
        //Establecemos el Tamaño de Cada Columna
        for (int i = 0; i < Tabla.getColumnCount(); i++)
            Tabla.getColumnModel().getColumn(i).setPreferredWidth(Anchos[i]);
    }
    
    public String ObtenerFechaHasta()
    {
        Calendar Calendario = Calendar.getInstance();
        return Calendario.get(Calendar.YEAR) + "-" + String.format("%02d", (Calendario.get(Calendar.MONTH) + 1)) + "-" + String.format("%02d", Calendario.get(Calendar.DATE));
    }
    
    public String ObtenerFechaDesde()
    {
        Calendar Calendario = Calendar.getInstance();
        return (Calendario.get(Calendar.YEAR)-1) + "-" + String.format("%02d", (Calendario.get(Calendar.MONTH) + 1)) + "-" + String.format("%02d", Calendario.get(Calendar.DATE));
    }
    
