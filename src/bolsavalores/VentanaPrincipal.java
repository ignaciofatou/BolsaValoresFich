    private void ActURLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActURLActionPerformed

        //Obtenemos la Ruta de los Datos y el Rango de Fechas
        String DireccionURL = URLDatos.getText().toString();
        String FechaDesde   = ObtenerFechaDesde();
        String FechaHasta   = ObtenerFechaHasta();

        //Si hay una URL Especificada
        if (!DireccionURL.equals("") && !FechaDesde.equals("") && !FechaHasta.equals(""))
        {
            DireccionURL = DireccionURL + FechaDesde + "/" + FechaHasta;            
            System.out.println("Direccion '"+DireccionURL+"'");
            
            // Abrimos la URL y Cargamos los Datos
            if (DatosHistoricos.IncluirHistoricosURL(DireccionURL, NombreValor, RedondeoValor) == true)
            {
                DatosHistoricos.CargaGrill(TablaHistorica);
                RellenaTecnico();
            }
        }
    }//GEN-LAST:event_ActURLActionPerformed

